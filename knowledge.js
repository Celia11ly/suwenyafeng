const SEASON_KEYS = ['春季', '夏季', '秋季', '冬季', '春天', '夏天', '秋天', '冬天'];
const ORGAN_KEYS = ['肝', '心', '脾', '肺', '肾', '胃', '肠'];

function detectType(topic) {
  if (INGREDIENT_KEYS.some(k => topic.includes(k))) return 'ingredient';
  if (SEASON_KEYS.some(k => topic.includes(k))) return 'season';
  if (ORGAN_KEYS.some(k => topic.includes(k))) return 'organ';
  return 'symptom';
}

// Smart fuzzy-match against KB
function getKnowledge(topic) {
  // Exact
  if (KB[topic]) return KB[topic];
  // Fuzzy: find best key overlap
  let best = null, bestScore = 0;
  for (const [k, v] of Object.entries(KB)) {
    const words = k.split('').filter(c => c.match(/[\u4e00-\u9fa5]/));
    const score = words.filter(c => topic.includes(c)).length;
    if (score > bestScore) { bestScore = score; best = v; }
  }
  if (bestScore >= 2) return best;
  // Fallback: synthesize from topic type
  return synthesize(topic);
}

